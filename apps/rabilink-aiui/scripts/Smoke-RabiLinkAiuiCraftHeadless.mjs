import fs from "node:fs";
import path from "node:path";
import { chromium } from "playwright-core";

const projectRoot = path.resolve(import.meta.dirname, "..");
const aixPath = path.join(projectRoot, "dist", "rabilink-aiui.aix");
const reportPath = path.join(projectRoot, "dist", "craft-headless-runtime.json");
const screenshotPath = path.join(projectRoot, "dist", "craft-headless-run.png");
const craftUrl = "https://js.rokid.com/craft?region=cn&lang=zh-CN";

function fail(message) {
  throw new Error(message);
}

function chromeExecutable() {
  const candidates = [
    process.env.CHROME_PATH,
    "C:/Program Files/Google/Chrome/Application/chrome.exe",
    "C:/Program Files (x86)/Google/Chrome/Application/chrome.exe"
  ].filter(Boolean);
  return candidates.find((candidate) => fs.existsSync(candidate)) || "";
}

function timeoutAfter(delayMs, label) {
  return new Promise((_, reject) => {
    setTimeout(() => reject(new Error(`${label} timed out after ${delayMs}ms.`)), delayMs);
  });
}

let browser;
let page;
const logs = [];
const pageErrors = [];
const report = {
  generatedAt: new Date().toISOString(),
  craftUrl,
  aixPath,
  aixSize: 0,
  imported: false,
  authenticated: false,
  runtimeSkippedReason: "",
  runClicked: false,
  cardCanvasSizes: [],
  cardCanvasObserved: false,
  responsiveProbes: 0,
  firstResponsiveProbeMs: null,
  enterClicked: false,
  immersiveCanvasObserved: false,
  immersiveResponsiveProbes: 0,
  immersiveFirstResponsiveProbeMs: null,
  modeRoundTrips: 0,
  configurationCanvasChanged: false,
  spinDiagnostics: [],
  layoutDiagnostics: [],
  openBundleCompleted: false,
  pageErrors,
  ok: false
};

function collectDiagnostics() {
  report.spinDiagnostics = logs.filter((line) => /apply_ops is still spinning|child_sync_parents/i.test(line));
  report.layoutDiagnostics = logs.filter((line) => /Attempted to add node as its own child|LayoutEngine::set_children/i.test(line));
  report.openBundleCompleted = logs.some((line) => /InkWebView::open_bundle completed/i.test(line));
  report.fatalLogs = logs
    .filter((line) => /Failed to execute (?:app\.js|page)|Exception generated by QuickJS|Error running page/i.test(line))
    .slice(-40);
  report.relevantLogs = logs
    .filter((line) => /open_bundle|onLoad|apply_ops|child_sync_parents|LayoutEngine|set_children|QuickJS|Failed to execute/i.test(line))
    .slice(-160);
}

try {
  if (!fs.existsSync(aixPath)) fail(`AIX not found: ${aixPath}. Run npm run package:aix first.`);
  report.aixSize = fs.statSync(aixPath).size;
  const executablePath = chromeExecutable();
  if (!executablePath) fail("Chrome was not found. Set CHROME_PATH to run the Craft headless smoke test.");

  browser = await chromium.launch({
    executablePath,
    headless: true,
    args: ["--no-sandbox", "--disable-dev-shm-usage", "--enable-unsafe-swiftshader", "--use-gl=angle", "--use-angle=swiftshader"]
  });
  page = await browser.newPage({ viewport: { width: 1500, height: 1000 }, deviceScaleFactor: 1 });
  page.on("console", (message) => logs.push(`${message.type()}: ${message.text()}`));
  page.on("pageerror", (error) => pageErrors.push(error.stack || error.message));

  await page.goto(craftUrl, { waitUntil: "domcontentloaded", timeout: 120000 });
  await page.locator('button[title="打开导入菜单"]').waitFor({ state: "visible", timeout: 30000 });
  await page.locator('button[title="打开导入菜单"]').click();
  const chooserPromise = page.waitForEvent("filechooser", { timeout: 10000 });
  await page.getByText("本地 .aix", { exact: true }).click();
  const chooser = await chooserPromise;
  await chooser.setFiles(aixPath);

  const runButton = page.getByRole("button", { name: "运行智能体", exact: true });
  await runButton.waitFor({ state: "visible", timeout: 30000 });
  await page.waitForFunction(() => {
    const button = [...document.querySelectorAll("button")].find((item) => item.textContent?.trim() === "运行智能体");
    return Boolean(button && !button.disabled);
  }, null, { timeout: 30000 });
  report.imported = true;

  report.authenticated = !(await page.evaluate(() => (
    [...document.querySelectorAll("button")].some((button) => button.textContent?.trim() === "未登录")
  )));
  if (!report.authenticated) {
    report.runtimeSkippedReason = "The isolated headless browser has no Craft login session; authenticated Run/Enter verification was not attempted.";
    await page.screenshot({ path: screenshotPath, fullPage: false });
    report.ok = pageErrors.length === 0;
    if (!report.ok) fail(`Craft import smoke failed: ${JSON.stringify(report)}`);
    console.log("RabiLink AIUI Craft import smoke passed; authenticated Run/Enter was skipped because the isolated browser is not logged in.");
    console.log("Use npm run interactive:resize for the deterministic production-Ink Run/Enter regression.");
    console.log(`Screenshot: ${screenshotPath}`);
  } else {

  const runStartedAt = Date.now();
  await runButton.click({ force: true, timeout: 5000 });
  report.runClicked = true;

  for (let index = 0; index < 8; index += 1) {
    await page.waitForTimeout(index === 0 ? 300 : 250);
    await Promise.race([
      page.evaluate(() => ({ now: performance.now(), title: document.title })),
      timeoutAfter(2000, `Craft responsiveness probe ${index + 1}`)
    ]);
    report.responsiveProbes += 1;
    if (report.firstResponsiveProbeMs == null) report.firstResponsiveProbeMs = Date.now() - runStartedAt;
  }

  await page.waitForFunction(() => (
    document.querySelectorAll("canvas").length > 0
      || [...document.querySelectorAll("*")].some((element) => element.textContent?.trim() === "进入")
  ), null, { timeout: 30000 });
  report.cardCanvasSizes = await page.evaluate(() => (
    [...document.querySelectorAll("canvas")].map((canvas) => ({
      width: canvas.width,
      height: canvas.height
    }))
  ));
  report.cardCanvasObserved = report.cardCanvasSizes
    .some((canvas) => canvas.width === 448 && canvas.height === 150);

  const enterButton = page.getByText("进入", { exact: true }).last();
  await enterButton.waitFor({ state: "visible", timeout: 30000 });
  const enterStartedAt = Date.now();
  await enterButton.click({ force: true, timeout: 5000 });
  report.enterClicked = true;

  for (let index = 0; index < 8; index += 1) {
    await new Promise((resolve) => setTimeout(resolve, index === 0 ? 200 : 250));
    const canvasSizes = await Promise.race([
      page.evaluate(() => [...document.querySelectorAll("canvas")].map((canvas) => ({
        width: canvas.width,
        height: canvas.height
      }))),
      timeoutAfter(2500, `Craft immersive responsiveness probe ${index + 1}`)
    ]);
    report.immersiveResponsiveProbes += 1;
    if (report.immersiveFirstResponsiveProbeMs == null) {
      report.immersiveFirstResponsiveProbeMs = Date.now() - enterStartedAt;
    }
    if (canvasSizes.some((canvas) => canvas.width === 480 && canvas.height === 352)) {
      report.immersiveCanvasObserved = true;
    }
  }

  const immersiveFingerprint = await page.evaluate(() => {
    const canvas = [...document.querySelectorAll("canvas")].find((item) => item.width === 480 && item.height === 352);
    if (!canvas) return "";
    canvas.tabIndex = 0;
    canvas.focus();
    return canvas.toDataURL("image/png");
  });
  for (let index = 0; index < 20; index += 1) {
    await page.keyboard.press("ArrowDown");
    await page.waitForTimeout(index === 0 ? 250 : 45);
    if (index === 0) {
      const configurationFingerprint = await page.evaluate(() => {
        const canvas = [...document.querySelectorAll("canvas")].find((item) => item.width === 480 && item.height === 352);
        return canvas?.toDataURL("image/png") || "";
      });
      report.configurationCanvasChanged = Boolean(configurationFingerprint && configurationFingerprint !== immersiveFingerprint);
    }
    await page.keyboard.press("ArrowUp");
    await page.waitForTimeout(45);
    await Promise.race([
      page.evaluate(() => ({ now: performance.now(), canvases: document.querySelectorAll("canvas").length })),
      timeoutAfter(2000, `Craft mode round-trip probe ${index + 1}`)
    ]);
    report.modeRoundTrips += 1;
  }

  collectDiagnostics();

  await page.screenshot({ path: screenshotPath, fullPage: false });
  report.ok = report.responsiveProbes === 8
    && report.cardCanvasObserved
    && report.enterClicked
    && report.immersiveResponsiveProbes === 8
    && report.immersiveCanvasObserved
    && report.modeRoundTrips === 20
    && report.configurationCanvasChanged
    && report.spinDiagnostics.length === 0
    && report.layoutDiagnostics.length === 0
    && report.openBundleCompleted
    && report.fatalLogs.length === 0
    && pageErrors.length === 0;
  if (!report.ok) fail(`Craft runtime did not meet the responsiveness contract: ${JSON.stringify(report)}`);

  console.log(`RabiLink AIUI Craft headless smoke passed (${report.responsiveProbes} card probes + ${report.immersiveResponsiveProbes} immersive probes + ${report.modeRoundTrips} mode round trips, no Ink layout diagnostics).`);
  console.log(`Screenshot: ${screenshotPath}`);
  }
} catch (error) {
  collectDiagnostics();
  report.hostSnapshot = await Promise.race([
    page?.evaluate(() => ({
      title: document.title,
      text: document.body?.innerText?.slice(0, 4000) || "",
      buttons: [...document.querySelectorAll("button")].map((button) => ({
        text: button.textContent?.trim() || "",
        disabled: button.disabled
      })).slice(0, 80)
    })),
    timeoutAfter(2000, "Craft failure snapshot")
  ]).catch((snapshotError) => ({ error: snapshotError.message }));
  await Promise.race([
    page?.screenshot({ path: screenshotPath, fullPage: false }),
    timeoutAfter(2000, "Craft failure screenshot")
  ]).catch(() => {});
  report.error = error?.stack || String(error);
  throw error;
} finally {
  fs.mkdirSync(path.dirname(reportPath), { recursive: true });
  fs.writeFileSync(reportPath, `${JSON.stringify(report, null, 2)}\n`, "utf8");
  await browser?.close().catch(() => {});
}
